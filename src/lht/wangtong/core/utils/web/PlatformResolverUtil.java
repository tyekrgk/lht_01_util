package lht.wangtong.core.utils.web;


public class PlatformResolverUtil {
	
	public static String getName (String language,String prefix){
		String name = "name";
		if ("NAME_ZH".equals(language)){
			name = "name";
		} else if ("NAME_EN".equals(language)){
			name = "name_en";
		} else if ("NAME_JA".equals(language)){
			name = "name_ja";
		} else if ("NAME_KO".equals(language)){
			name = "name_ko";
		} else if ("NAME_DEF1".equals(language)){
			name = "name_def1";
		} else if ("NAME_DEF2".equals(language)){
			name = "NAME_DEF2";
		} else if ("NAME_DEF3".equals(language)){
			name = "NAME_DEF3";
		} else if ("NAME_DEF4".equals(language)){
			name = "NAME_DEF4";
		} else if ("NAME_DEF5".equals(language)){
			name = "NAME_DEF5";
		} else if ("NAME_DEF6".equals(language)){
			name = "NAME_DEF6";
		}
		return prefix + "." + name;
	}

}
